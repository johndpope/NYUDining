import Foundation
import UIKit

extension UIColor {
    public static func navColor() -> UIColor {
        return UIColor(red: 0.376, green: 0.173, blue: 0.565, alpha: 1.00)
    }
    
    public static func tabColor() -> UIColor {
        return UIColor(red: 0.376, green: 0.173, blue: 0.565, alpha: 1.00)
    }
}